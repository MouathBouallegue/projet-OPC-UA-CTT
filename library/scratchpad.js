function uaObject( args ) {
    this.DisplayName = ( args !== undefined && args.DisplayName !== undefined ? args.DisplayName : "" );
    this.BrowseName  = ( args !== undefined && args.BrowseName  !== undefined ? args.BrowseName  : "" );
    this.Description = ( args !== undefined && args.Description !== undefined ? args.Description : "" );
    this.NodeId      = ( args !== undefined && args.NodeId      !== undefined ? args.NodeId      : null );
    this.References  = ( args !== undefined && args.References  !== undefined ? args.References  : [] );

    this.toJson = function() { return( JSON.stringify( this ) ); }
};

function findReference( nodeId ) {
    if( nodeId == undefined ) return( null );
    for( var i=0; i<uadatatypes.length; i++ )      if( nodeId.toString() == uadatatypes[i].NodeId.toString() )      return( uadatatypes[i] );
    for( var i=0; i<uamethods.length; i++ )        if( nodeId.toString() == uamethods[i].NodeId.toString() )        return( uamethods[i] );
    for( var i=0; i<uavariabletypes.length; i++ )  if( nodeId.toString() == uavariabletypes[i].NodeId.toString() )  return( uareferencetypes[i] );
    for( var i=0; i<uavariables.length; i++ )      if( nodeId.toString() == uavariables[i].NodeId.toString() )      return( uavariables[i] );
    for( var i=0; i<uavariabletypes.length; i++ )  if( nodeId.toString() == uavariabletypes[i].NodeId.toString() )  return( uavariabletypes[i] );
    for( var i=0; i<uaobjecttypes.length; i++ )    if( nodeId.toString() == uaobjecttypes[i].NodeId.toString() )    return( uaobjecttypes[i] );
}

function getReferences( references, level ) {
    if( references == undefined || references == null || references.length == undefined || references.length == null || references.length == 0 ) return( [] );
    if( level == undefined || level == null || level > 10 ) return( null );
    var refObjects = [];
    for( var r=0; r<references.length; r++ ) {
        // we only care about forward references (we're building a structure from the top-down, so we don't care about "also" having bottom-up
        if( references[r].IsForward == "true" ) {
            var targetRef = findReference( references[r].NodeId );
            if( targetRef !== undefined && targetRef !== null ) {            
                targetRef.ReferenceTypeId = "Identifier." + references[r].ReferenceTypeId;
                targetRef.NodeId = references[r].NodeId;
                refObjects.push( targetRef );
            }
        }
    }//for r...
    return( refObjects );
}

function getObject( nodeId ) {
    for( var i=0; i<uaobjecttypes.length; i++ ) {
        if( nodeId.toString() == uaobjecttypes[i].NodeId ) {
            var o = new uaObject( { DisplayName: uaobjecttypes[i].DisplayName, BrowseName: uaobjecttypes[i].BrowseName, NodeId: uaobjecttypes[i].NodeId, Description: uaobjecttypes[i].Description } );
            // go get the object's references
            o.References = getReferences( uaobjecttypes[i].References, 1 );
            // remove the object from the collection because we will not need it again, and it'll just slow-down the remaining searches
            uaobjecttypes.splice( i, 1 );
            // are there any sub-objects defined?
            var subObjects = getSubObjects( nodeId );
            if( subObjects !== undefined && subObjects !== null && subObjects.length !== undefined && subObjects.length > 0 ) {
                o.SubObjects = [];
                // for each object, dig-deeper still...
                for( var s=0; s<subObjects.length; s++ ) {
                    var subObject = getObject( subObjects[s].NodeId );
                    o.SubObjects.push( subObject );
                }
            }
            return( o );
        }
    }// for i...
}

function getSubObjects( nodeId ) {
    // this is a process... we have to do the following: 
    // a) search through ALL objects, and for each...
    // b) search through ALL references
    // c) locate any INVERSE reference whose NodeId matches the parameter
    var objects = [];
    for( var i=0; i<uaobjecttypes.length; i++ ) {                                                                // iterate through each object
        if( uaobjecttypes[i].References !== undefined && uaobjecttypes[i].References !== null ) {                // we only care about objects with references
            for( var r=0; r<uaobjecttypes[i].References.length; r++ ) {                                          // iterate through each reference
                if( uaobjecttypes[i].References[r].ReferenceTypeId == "HasSubtype" && uaobjecttypes[i].References[r].IsForward == "false" ) {  // inverse reference?
                    if( uaobjecttypes[i].References[r].NodeId == nodeId ) objects.push( uaobjecttypes[i] );      // looks like a match, bag it!
                }// inverse reference?
            }// for each reference
        }// if references exist for this object
    }// for each object
    return( objects );
}

